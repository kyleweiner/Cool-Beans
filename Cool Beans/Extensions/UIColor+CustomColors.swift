extension UIColor {
    class func CBHotColor() -> UIColor {
        return UIColor(red: 1, green: 0.157, blue: 0.318, alpha: 1)
    }

    class func CBWarmColor() -> UIColor {
        return UIColor(red: 1, green: 0.588, blue: 0, alpha: 1)
    }

    class func CBCoolColor() -> UIColor {
        return UIColor(red: 1, green: 0.804, blue: 0, alpha: 1)
    }

    class func CBColdColor() -> UIColor {
        return UIColor(red: 0.329, green: 0.78, blue: 0.988, alpha: 1)
    }
}